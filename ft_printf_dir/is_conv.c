#include "ft_printf.h"

int		is_conv(char c)
{
	if (c == 'c' || c == 's' || c == 'p' ||\
		c == 'd' || c == 'i' || c == 'u' ||\
		c == 'x' || c == 'X')
		return (1);
	return (0);
}
